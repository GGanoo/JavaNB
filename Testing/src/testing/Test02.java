package testing;

public class Test02 {
	// 연습문제 3: 양수, 음수, 0 판별
	public static void numberChecker(int number) {
        // 양수, 음수, 0을 구분하여 출력
        if (number > 0) {
            System.out.println("양수입니다.");
        } else if (number < 0) {
            System.out.println("음수입니다.");
        } else {
            System.out.println("0입니다.");
        }
    }
	public static void main(String[] args) {
		
		int number = 5;
	}
	}
	
    
