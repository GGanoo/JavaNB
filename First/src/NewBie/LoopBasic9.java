package NewBie;

public class LoopBasic9 {

	public static void main(String[] args) {
		  
		// for 
		// 2 * 1 = 2
		// 2 * 9 = 18 
		for (int i = 2; i < 10; i++) {
			for (int j = 1; j <= 9; j++) {
				
				
				System.out.println(i + " * " + j + " = " + (i * j)); }
			
			System.out.println("");
	}
		
		
		
	}
}
